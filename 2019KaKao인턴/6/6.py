import re


def solution(word, pages):
    answer = 0
    max_match=0
    word =word.upper()
    pages_temp = pages
    w = re.compile("[^A-Za-z]+")
    basic={}
    p_url = {}
    for i,p in enumerate(pages_temp):
        p=p.upper()
        p=w.sub(' ',p)
        p=p.split()
        if i not in basic.keys():
            basic[i] = 0
        for k in p :
            if k==word :
                basic[i]+=1

    u = re.compile("<meta property=\"og:url\" content=\"(.*)\"\/>")
    for i,p in enumerate(pages):
        url = u.findall(p)
        p_url[url[0]]=i

    o = re.compile("<a href=\"([^<]*)\">")
    ext_url={}
    ext_count={}
    for i,p in enumerate(pages):
        url = o.findall(p)
        ext_count[i] = len(url)
        for u in url:
            if u not in p_url.keys():
                continue
            x = p_url[u] 
            if x not in ext_url.keys():
                ext_url[x]= []
            ext_url[x].append(i)

    for i in range(len(basic)) :
        match=0
        match+=basic[i]
        if i in ext_url.keys() :
            for e in ext_url[i] :
                match+=basic[e]/ext_count[e]
        if match > max_match :
            answer=i
            max_match=match
    
    return answer

